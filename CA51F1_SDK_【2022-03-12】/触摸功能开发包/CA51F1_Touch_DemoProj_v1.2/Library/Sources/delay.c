void Delay_50us(unsigned int n)	   
{
	unsigned char i;	
#ifdef SYSCLK_16MHZ
	n *= 2;
#endif
	while(n--)
	{
		for(i=0;i<33;i++);
	}
}
void Delay_ms(unsigned int delay_nms)
{
	while(delay_nms--)
	{
		Delay_50us(20);
	}
}
